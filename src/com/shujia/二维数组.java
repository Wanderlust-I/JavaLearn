package com.shujia;

public class 二维数组 {
    public static void main(String[] args) {
        int [][]arr=new int[][]{{1,2,3},{12,4,5,},{1,3,4},{1,3,5}};;
        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j <arr[i].length; j++) {
                System.out.println(arr[i][j]);
            }
        }
    }



}
